GROUP BY name
ORDER BY name ASC;
END$$

CREATE DEFINER=`root`@`localhost` PROCEDURE `findPrice` (IN `lower` INT, IN `upper` INT, IN `category` VARCHAR(50))   BEGIN
SELECT it.item_name, it.price, ic.category_name
FROM items it
INNER JOIN item_categories ic
ON ic.category_id=it.category_id
WHERE it.price BETWEEN lower AND upper
AND ic.category_name LIKE CONCAT('%', category, '%')
ORDER BY price DESC;
END$$

CREATE DEFINER=`root`@`localhost` PROCEDURE `GetVAT` (IN `category` VARCHAR(50), IN `ordervar` VARCHAR(9))   BEGIN
