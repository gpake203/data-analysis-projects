FROM items it
INNER JOIN item_categories ic
ON ic.category_id=it.category_id
WHERE ic.category_name LIKE CONCAT('%', category, '%')
ORDER BY CASE WHEN ordervar='item_name' THEN it.item_name END ASC,
CASE WHEN ordervar='price' THEN it.price END DESC;
END$$

